//tipo de função

//declarativas
function funcao(){
    console.log('Sou um mensagem de uma função declarativa!');
}
funcao();

//Expressões de Funções
//com nomeação
var funcao = function (){
    console.log('Sou um mensagem de uma função de expressão');
}
funcao();


//arrow function
var funcao = () => {
    console.log('Sou uma arrow function');
}

funcao();

